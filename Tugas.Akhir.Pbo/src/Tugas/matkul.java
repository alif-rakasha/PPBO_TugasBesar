    //menangani proses saat button save di klik
    private void jButtonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveActionPerformed
        // TODO add your handling code here:
        String IDMatkul=jTextIDMatkul.getText();
        String NamaMatkul=jTextNama.getText();
        String Sks=jComboBoxSks.getSelectedItem().toString();
        String Deadline=jTextDeadline.getText();
        String Kelas=jTextKelas.getText();
        jButtonAdd.setEnabled(true);

        if (IDMatkul.isEmpty() ) {
            JOptionPane.showMessageDialog(null,"Kode Mata Kuliah tidak boleh kosong");
            jTextIDMatkul.requestFocus();
        }else if (NamaMatkul.isEmpty()) {
            JOptionPane.showMessageDialog(null,"Nama Mata Kuliah tidak boleh kosong");
            jTextNama.requestFocus();
        }else if (Sks.isEmpty()) {
            JOptionPane.showMessageDialog(null,"Jumlah SKS tidak boleh kosong");
            jComboBoxSks.requestFocus();
        }else if (Deadline.isEmpty()) {
            JOptionPane.showMessageDialog(null,"Deadline tidak boleh kosong");
            jTextDeadline.requestFocus();
        }else if (Kelas.isEmpty()) {
            JOptionPane.showMessageDialog(null,"Kelas tidak boleh kosong");
            jTextKelas.requestFocus();
        }else{
            try{
                Connection conn=(Connection)koneksi.koneksiDB();
                Statement stt=conn.createStatement();
                stt.executeUpdate("insert into matkul(IDMatkul,NamaMatkul,SKS,Deadline,Kelas)"+
                    "VALUES('"+IDMatkul+"','"+NamaMatkul+"','"+Sks+"','"+Deadline+"','"+Kelas+"')");
                BersihData();
                tampilData();
                SetEditOff();
                JOptionPane.showMessageDialog(this,"Data berhasil disimpan","Success",JOptionPane.INFORMATION_MESSAGE);
            } catch(SQLException e){
                JOptionPane.showMessageDialog(this,"Simpan data gagal\n"+e.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
            }
        }

    }//GEN-LAST:event_jButtonSaveActionPerformed

    //menangani proses saat button Update di klik
    private void jButtonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUpdateActionPerformed
        // TODO add your handling code here:
        String IDMatkul=jTextIDMatkul.getText();
        String NamaMatkul=jTextNama.getText();
        String Sks=jComboBoxSks.getSelectedItem().toString();
        String Deadline=jTextDeadline.getText();
        String Kelas=jTextKelas.getText();

        if (IDMatkul.isEmpty() ) {
            JOptionPane.showMessageDialog(null,"Kode Mata Kuliah tidak boleh kosong");
            jTextIDMatkul.requestFocus();
        }else if (NamaMatkul.isEmpty()) {
            JOptionPane.showMessageDialog(null,"Nama Mata Kuliah tidak boleh kosong");
            jTextNama.requestFocus();
        }else if (Sks.isEmpty()) {
            JOptionPane.showMessageDialog(null,"Jumlah SKS tidak boleh kosong");
            jComboBoxSks.requestFocus();
        }else if (Deadline.isEmpty()) {
            JOptionPane.showMessageDialog(null,"Deadline tidak boleh kosong");
            jTextDeadline.requestFocus();
        }else if (Kelas.isEmpty()) {
            JOptionPane.showMessageDialog(null,"Kelas tidak boleh kosong");
            jTextKelas.requestFocus();
        }
        else{
            try{
                Connection conn=(Connection)koneksi.koneksiDB();
                Statement stt=conn.createStatement();
                stt.executeUpdate("UPDATE matkul SET NamaMatkul ='"+NamaMatkul+"', SKS ='"+Sks+"', Deadline ='"+Deadline
                        +"', Kelas ='"+Kelas+"' WHERE IDMatkul ='"+IDMatkul+"'");
                BersihData();
                tampilData();
                SetEditOff();
                JOptionPane.showMessageDialog(this,"Data berhasil diubah","Success",JOptionPane.INFORMATION_MESSAGE);
            } catch(SQLException e){
                JOptionPane.showMessageDialog(this,"Ubah data gagal\n"+e.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
            }
        }

    }//GEN-LAST:event_jButtonUpdateActionPerformed

    //menangani proses saat button delete di klik
    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        // TODO add your handling code here:
        String IDMatkul=jTextIDMatkul.getText();
        jButtonAdd.setEnabled(true);

        if (IDMatkul.isEmpty() ) {
            JOptionPane.showMessageDialog(null,"Kode Mata Kuliah tidak boleh kosong");
            jTextIDMatkul.requestFocus();
        }else if(JOptionPane.showConfirmDialog(null,"Apakah anda yakin akan menghapus data ini?",
            "Informasi",JOptionPane.OK_CANCEL_OPTION,JOptionPane.INFORMATION_MESSAGE)==JOptionPane.OK_OPTION){
        try{
            Connection conn=(Connection)koneksi.koneksiDB();
            Statement stt=conn.createStatement();
            stt.executeUpdate("DELETE FROM matkul WHERE IDMatkul='"+IDMatkul+"'");
            BersihData();
            tampilData();
            SetEditOff();
            JOptionPane.showMessageDialog(this,"Data berhasil di hapus","Success",JOptionPane.INFORMATION_MESSAGE);
        } catch(SQLException e){
            JOptionPane.showMessageDialog(this,"Delete data gagal\n"+e.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
        }
        }
    }//GEN-LAST:event_jButtonDeleteActionPerformed

