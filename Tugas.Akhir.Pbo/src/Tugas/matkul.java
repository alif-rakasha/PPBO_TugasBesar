    //fungsi untuk menampilkan data matkul ke dalam tabel
    private void tampilData(){
        try{
            Object[] judul_kolom = {"Kode Mata Kuliah", "Nama Mata Kuliah", "Jumlah SKS", "Deadline", "Kelas"};
            tabModel=new DefaultTableModel(null,judul_kolom);
            TabelMatkul.setModel(tabModel);
            
            Connection conn=(Connection)koneksi.koneksiDB();
            Statement stt=conn.createStatement();
            tabModel.getDataVector().removeAllElements();
            
            RsMatkul=stt.executeQuery("SELECT * from matkul ");  
            while(RsMatkul.next()){
                Object[] data={
                    RsMatkul.getString("IDMatkul"),
                    RsMatkul.getString("NamaMatkul"),
                    RsMatkul.getString("SKS"),
                    RsMatkul.getString("Deadline"),
                    RsMatkul.getString("Kelas"),        
                };
               tabModel.addRow(data);
            }                
        } catch (Exception ex) {
        System.err.println(ex.getMessage());
        }
        // disable button
        jButtonSave.setEnabled(false);
        jButtonUpdate.setEnabled(false);
        jButtonDelete.setEnabled(false);
        jButtonCancel.setEnabled(false);
    }
   
    //menampilkan data ke form saat data pada tabel di klik 
   void tblKeForm(){
        jTextIDMatkul.setText(tabModel.getValueAt(TabelMatkul.getSelectedRow(),0)+"");
        jTextNama.setText(tabModel.getValueAt(TabelMatkul.getSelectedRow(),1)+"");
        jComboBoxSks.setSelectedItem(tabModel.getValueAt(TabelMatkul.getSelectedRow(),2)+"");
        jTextDeadline.setText(tabModel.getValueAt(TabelMatkul.getSelectedRow(),3)+"");
        jTextKelas.setText(tabModel.getValueAt(TabelMatkul.getSelectedRow(),4)+"");
        
        jButtonUpdate.setEnabled(true);
        jButtonDelete.setEnabled(true);
        jButtonCancel.setEnabled(true);
        jButtonSave.setEnabled(false);
    }
   
